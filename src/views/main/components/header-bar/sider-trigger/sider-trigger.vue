<template>
  <a :class="['sider-trigger-a', collapsed ? 'collapsed' : '']" @click="handleChange" type="text">
    <Icon :size="size" :type="icon"/>
  </a>
</template>
<script>
export default {
  name: 'siderTrigger',
  props: {
    collapsed: Boolean,
    icon: {
      type: String,
      default: 'md-menu'
    },
    size: {
      type: Number,
      default: 26
    }
  },
  methods: {
    handleChange () {
      this.$emit('on-change', !this.collapsed)
    }
  }
}
</script>
